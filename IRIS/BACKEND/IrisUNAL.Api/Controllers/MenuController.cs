            }

        }
        [HttpDelete]
        public IHttpActionResult DeleteMenu(int idmenu)
        {
            var resultdb = new ResultObject();
            try
            {
                //***VALIDAR LAS REGLAS DE BORRADO

                var data = _menuRepository.DeleteMenu(idmenu);

